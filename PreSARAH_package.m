CombinePhotonDiagrams:=Block[{i,j},
(* The Photon diagrams are numerically a bit delicate *)
(* Therefore, we sum up here the wave and the two possible penguin diagrams and simplify them analytically *)

(* The electric charges are choosen as follows:  *)
(*     - external Fermion: Qex   *)
(*     - internal Fermion: QF   *)
(*     - internal Scalar: QS   *)
(*     - internal Vector: QV   *)

(* In addition, we know that Photon vertices can't change the nature of the particle, i.e. we have just two unknown masses in the Penguin diagrams *)


PhotonSumScalar=Table[{},{Length[PhotonPenguinOperators]}];
PhotonSumVector=Table[{},{Length[PhotonPenguinOperators]}];

For[i=1,i<=Length[PhotonPenguinOperators],
PhotonSumScalar[[i]]={{PhotonPenguinOperators[[i,1]],(PenguinAmplitudes[[3,1,1,2,i,2]] /. {coup3L->QF,coup3R->QF,mF22->mF12,mF2->mF1})+(PenguinAmplitudes[[3,1,2,2,i,2]]/. {coup3->QS,mS22->mS12,mS2->mS1})+(WaveAmplitudes[[3,1,1,2,i,2]] /. {coup1L->Qex,coup1R->Qex,coup2L->coup1R,coup2R->coup1L,coup3L->coup2R,coup3R->coup2L, MFin->MassB, MFin2->MassB2}) //.{ vertexC12aux->vertexC12Ev, vertexC11aux->vertexC11Ev, vertexC0tildeaux->vertexC0tildeEv, C0[0,0,0,a_,b_,c_]->C03mEv[a,b,c],bb0[0,a_,b_]:>B0Ev[a,b],bb1[0,a_,b_]:>B1Ev[a,b]}}};
PhotonSumVector[[i]]={{PhotonPenguinOperators[[i,1]],(PenguinAmplitudes[[3,1,3,2,i,2]] /. {coup3L->QF,coup3R->QF,mF22->mF12,mF2->mF1})+(PenguinAmplitudes[[3,1,6,2,i,2]]/. {coup3->QV,mV22->mV12,mV2->mV1})+(WaveAmplitudes[[3,1,2,2,i,2]] /. {coup1L->Qex,coup1R->Qex,coup2L->coup1L,coup2R->coup1R,coup3L->coup2L,coup3R->coup2R, MFin->MassB, MFin2->MassB2}) //.{ vertexC12aux->vertexC12Ev, vertexC11aux->vertexC11Ev, vertexC0tildeaux->vertexC0tildeEv,C0[0,0,0,a_,b_,c_]->C03mEv[a,b,c],bb0[0,a_,b_]:>B0Ev[a,b],bb1[0,a_,b_]:>B1Ev[a,b]}}};
i++;];
];

vertexC12Ev[a_,b_,b_]:=-(3 a^2-4 a*b+b^2- a^2*Log[a/b])/(4*(a-b)^3);
vertexC12Ev[a_,a_,b_]:=vertexC12Ev[b,a,a];

vertexC11Ev[a_,b_,b_]:=-(3*a^2-4*a b+b^2-2*a^2*Log[a/b])/(2*(a-b)^3);
vertexC11Ev[a_,a_,b_]:=vertexC11Ev[b,a,a];

C03mEv[m1_,m2_,m2_]:=(1-m1/(m2-m1)*Log[m2/m1])/(m1-m2);
C03mEv[m2_,m2_,m1_]:=C03mEv[m1,m2,m2];
vertexC0tildeEv[m1_,m2_,m2_]:=(m1*(m1-m2)-m1^2 Log[m1]+(2 m1-m2) m2 Log[m2])/(m1-m2)^2 + 1/2;
vertexC0tildeEv[a_,a_,b_]:=vertexC0tildeEv[b,a,a];
B1Ev[m1_,m2_]:=-1/2 + 1/2 Log[m2] - (m1^2 - m2^2 +2 m1^2 Log[m2/m1])/(4(m1 - m2)^2); 
B0Ev[m1_,m1_]:=- Log[m1] ;
B0Ev[m1_,m2_]:=- Log[m2] + (m2 - m1 + m1 Log[m1/m2])/((m2 - m1)) /; (m1=!=m2);  

